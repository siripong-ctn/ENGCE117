#include <stdio.h>

struct Product {
    char name_lol[50];
    int productId;
    float price;
};

int main() {
    
    struct Product item;
    
    if (scanf("%d %f %s", &item.productId, &item.price, item.name_lol) != 3) {
        return 1;
    }

    printf("ID: %d ,", item.productId);
    printf(" Price: %.2f ,", item.price);
    printf(" Name: %s \n", item.name_lol);
    
    return 0; // มันแปลว่าโปรแกรมสิ้นสุดการทำงานแล้วจ้า

    // วิธีทำ “ข้าวมันไก่” พร้อมสูตรน้ำจิ้มรสเด็ด เปิดร้านขายกำไรงาม! อ่านต่อได้ที่ https://www.wongnai.com/recipes/hainanese-chicken-rice?ref=ct
    // “ข้าวมันไก่” อาหารจานเดียวเร่งด่วนกินง่ายกินเมื่อไหร่ทั้งอิ่มทั้งฟิน เมนูจานโปรดของใครหลาย ๆ คน ที่จริงทำไม่ยากเลยนะคะ อมแล้วตามมาดูสูตรและวิธีทำข้าวมันไก่กันเลย!
    // แต่ต้องมีเคล็ดลับในการทำเล็ก ๆ น้อย ๆ ก็จะได้รสชาติที่ออกมาเหมือนไปนั่งกินที่ร้านเลยค่า ถ้าพร้อมแล้วเราไปเริ่มกันเลย!
    // ส่วนผสม ข้าวมันไก่ (สำหรับ 2 ที่)
    // ข้าวสารหอมมะลิ 2 ถ้วย
    // น้ำมันไก่ 2 ช้อนโต๊ะ
    // กระเทียมสับ 1 ช้อนโต๊ะ
    // รากผักชีสับ 1 ช้อนโต๊ะ
    // น้ำซุปไก่ 2 ½ ถ้วย
    // เนื้อไก่ (น่องติดสะโพก) 2 ชิ้น
    // ขิงแก่หั่นแว่น 5 แว่น
    // ต้นหอม 2 ต้น
    // เกลือป่น 1 ช้อนชา
    // น้ำจิ้มข้าวมันไก่
    // พริกขี้หนูสวน 10 เม็ด
    // กระเทียม 5 กลีบ
    // ขิงแก่สับ 1 ช้อนโต๊ะ
    // น้ำตาลทราย 1 ช้อนชา
    // น้ำส้มสายชู 2 ช้อนโต๊ะ
    // ซีอิ๊วขาว 2 ช้อนโต๊ะ
    // วิธีทำข้าวมันไก่
    // ล้างข้าวสารให้สะอาด ซาวน้ำจนใส สะเด็ดน้ำพักไว้
    // ตั้งหม้อบนเตา ใส่น้ำมันไก่ลงไป ตามด้วยกระเทียมสับและรากผักชีสับ ผัดจนหอม
    // ใส่ข้าวสารลงไปผัดให้เข้ากัน เติมน้ำซุปไก่ลงไป รอจนน้ำเดือด ลดไฟลงเป็นไฟอ่อน ปิดฝาหม้อ ต้มข้าวจนสุก ใช้เวลาประมาณ 15-20 นาที
    // ระหว่างรอข้าวสุก นำเนื้อไก่ลงไปต้มในหม้อน้ำเดือดที่ใส่ขิงแก่และต้นหอมลงไปด้วย ต้มจนเนื้อไก่สุกดี ใช้เวลาประมาณ 30 นาที
    // ตักเนื้อไก่ขึ้นมาแช่ในน้ำเย็นจัดเพื่อให้เนื้อไก่แน่นขึ้น พักไว้ให้สะเด็ดน้ำ
    // ทำน้ำจิ้มข้าวมันไก่โดยโขลกพริกขี้หนูสวนกับกระเทียมให้ละเอียด ใส่ขิงสับลงไปโขลกพอเข้ากัน
    // ปรุงรสด้วยน้ำตาลทราย น้ำส้มสายชู และซีอิ๊วขาว คนให้เข้ากัน ชิมรสตามชอบ
    // หั่นเนื้อไก่เป็นชิ้นพอดีคำ จัดเสิร์ฟพร้อมข้าวมัน ราดด้วยน้ำจิ้มข้าวมันไก่ พร้อมน้ำซุปที่ต้มไก่ไว้
}